  case 0x2C: return "," ;
  case 0x2D: return "-" ;
  case 0x2E: return "." ;
  case 0x2F: return "/" ;
  case 0x30: return "0" ;
  case 0x31: return "1" ;
  case 0x32: return "2" ;
  case 0x33: return "3" ;
  case 0x34: return "4" ;
  case 0x35: return "5" ;
  case 0x36: return "6" ;
  case 0x37: return "7" ;
  case 0x38: return "8" ;
  case 0x39: return "9" ;
  case 0x3A: return ":" ;
  case 0x3B: return ";" ;
  case 0x3C: return "<" ;
  case 0x3D: return "=" ;
  case 0x3E: return ">" ;
  case 0x3F: return "?" ;
